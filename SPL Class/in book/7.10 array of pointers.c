/*যদি স্ট্রিংগুলো একটি দ্বি-মাত্রিক অ্যারেতে সংরক্ষণ করা হত, তাহলে অ্যারের প্রত্যেকটি সারি একটি স্যুট (suit)
এবং প্রত্যেকটি কলাম স্ট্রিংয়ের একটি লেটার ধারণ করত। এর ফলে অ্যারেটির একটি নির্দিষ্ট কলাম সংখ্যা থাকা প্রয়োজন
হত, যা স্ট্রিংগুলোর মধ্যে সবচেয়ে বড় স্ট্রিংটির দৈর্ঘ্যের সমান হতে হত। ফলে, যখন অনেক ছোট স্ট্রিং সংরক্ষণ করা হত
তখন অনেক মেমোরি নষ্ট হত। এ জন্য স্ট্রিং পয়েন্টার অ্যারে ব্যবহার করাটা বেশি কার্যকরী।*/
#include <stdio.h>

int main() {
    // Declare a constant array of character pointers (string array)
    const char *suit[4] = {"Hearts", "Diamonds", "Clubs", "Spades"};

    // Print each suit using a loop
    for (int i = 0; i < 4; i++) {
        printf("Suit %d: %s\n", i + 1, suit[i]);
    }

    return 0;
}
